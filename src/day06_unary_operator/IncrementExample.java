package day06_unary_operator;

public class IncrementExample {
    public static void main(String[] args) {
      int x = 6;
        System.out.println(x);
        x= x+1 ;
        System.out.println(x);
        x++;
        System.out.println(x);
        ++x;
        System.out.println(++x);
        x++;
        System.out.println(x);
        System.out.println(x++);

    }





}
